# Different Data Types Examples
def data_types_examples():
    print("Data Types Examples:")
    integer_example = 10  # Integer
    float_example = 10.5  # Float
    string_example = "Hello, World!"  # String
    boolean_example = True  # Boolean
    list_example = [1, 2, 3, 4, 5]  # List
    tuple_example = (6, 7, 8, 9, 10)  # Tuple
    set_example = {11, 12, 13, 14, 15}  # Set
    dict_example = {"name": "Alice", "age": 25}  # Dictionary
    
    print(f"Integer: {integer_example}")
    print(f"Float: {float_example}")
    print(f"String: {string_example}")
    print(f"Boolean: {boolean_example}")
    print(f"List: {list_example}")
    print(f"Tuple: {tuple_example}")
    print(f"Set: {set_example}")
    print(f"Dictionary: {dict_example}\n")

    
data_types_examples()
